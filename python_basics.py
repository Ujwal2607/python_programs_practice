# Assignment operator

a = 10
print("The value of a:{}".format(a))

b = 15
print("The value of b:{}".format(b))

a = 20

print("The value of a after replacing {}".format(a))

# Sum of a and b

sum_a_b = a+b
print("The sum of a and b {}".format(sum_a_b))

diff_a_b = a-b
print("The difference of a and b {}".format(diff_a_b))

prod_a_b = a*b
print("The product of a and b {}".format(prod_a_b))

quot_a_b = a/b
print("The quotient of a divided by b is {}".format(quot_a_b))

rem_a_b = a%b
print("The reminder of a divided by b is {}".format(rem_a_b))